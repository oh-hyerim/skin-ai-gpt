import { OpenAI } from "openai";

export default async function handler(req, res) {
  const openai = new OpenAI({
    apiKey: process.env.OPENAI_API_KEY,
  });

  const {
    skinType = "정보 없음",
    sensitive = "정보 없음",
    trouble = "정보 없음",
    barrier = "정보 없음",
    routineSummary = "정보 없음",
    choiceSummary = "정보 없음",
    productSummary = "정보 없음",
  } = req.body;

  const prompt = `
당신은 피부과 상담 전문가입니다.  
아래는 한 사용자의 피부 진단 결과이며, 해당 정보를 바탕으로 **항목별로 분석과 맞춤형 조언을 작성해주세요**.

분석 시 주의사항:  
- 반드시 **제공된 정보만 기반**으로 분석해주세요.  
- **모호한 조언, 피상적인 문장, 뻔한 상식**은 피해주세요.  
- **피부 타입별 특징에 따른 구체적, 현실적, 유용한 분석과 관리법**을 제공해주세요.  
- **"피부과 상담이 필요합니다"**, **"정확한 정보가 없어 분석이 어렵습니다"** 같은 회피성 문장은 금지합니다.  
- 정보가 부족할 경우에는 "**어떤 경우에 주의가 필요한지**", "**현재 상태에서 더 나은 관리를 위한 예시**" 등을 구체적으로 제시해주세요.
- 분석 시, 피부과 논문과 전문적 문헌 기반 지식을 활용하되, 이를 직접 인용하거나 복잡하게 표현하지 말고, 일반 사용자가 이해하기 쉽게 간접적으로 반영해주세요.
- 제품 성분 분석 시, 논문 기반 피부과 성분 효능을 토대로 '이 성분은 이럴 때 효과적'이라는 맥락으로 설명.
- 피부 장벽 설명 시, "지질층 손상은 수분 증발과 외부 자극 증가를 유도할 수 있다"는 논문 기반 지식을 친절하게 풀어서 설명.

사용자 피부 정보:
- 피부 타입: ${skinType || "정보 없음"}
- 민감도: ${sensitive || "정보 없음"}
- 트러블 발생 정도: ${trouble || "정보 없음"}
- 장벽/탄력 상태: ${barrier || "정보 없음"}
- 루틴 예/아니오 응답: ${routineSummary || "정보 없음"}
- 루틴 선택형 응답: ${choiceSummary || "정보 없음"}
- 현재 사용하는 제품 정보: ${productSummary || "정보 없음"}

분석 항목:

1. **피부 타입 분석**  
- 사용자의 피부 타입이 건성, 지성, 복합성, 수부지, 민감성, 중성, 노화성, 과각질성 등 어떤 유형에 더 가까운지 명확히 판단.  
- 해당 피부 타입의 주요 특징, 현재 피부 상태와 연결된 원인, 계절이나 환경에 따른 변화 가능성까지 분석.  
- 적절한 일상 관리 방향, 회피해야 할 루틴도 포함.

2. **민감도 분석**  
- 민감도가 높은 경우: 피부가 민감해지는 원인(예: 피부 장벽 손상, 자극 루틴, 환경적 요인 등)을 분석하고 예방 및 개선 방향 제시.  
- 민감도가 낮은 경우: 현재 상태를 잘 유지하기 위한 주의사항 (예: 자외선, 과도한 세안, 향료 사용 등)을 피부 타입에 맞춰 설명.

3. **트러블 경향**  
- 트러블 수치 및 루틴 응답을 기반으로 여드름, 좁쌀, 피지, 염증 트러블 발생 가능성을 분석.  
- 루틴 또는 제품 사용에서 트러블을 유발할 수 있는 습관, 성분, 루틴 순서를 지적하고 대안을 제시.  
- 트러블이 잘 나는 부위가 있을 경우 그 부위의 특징도 함께 설명.

4. **피부 장벽 및 탄력 상태**  
- 피부 장벽이 무너진 상태인지, 탄력이 저하된 상태인지 명확히 평가하고 그 원인 유추.  
- 장벽 강화를 위한 루틴 또는 제품 성분 제안 (예: 세라마이드, 판테놀, 마데카소사이드 등).  
- 탄력이 저하된 경우 일상 속 관리 요령(마사지, 제품 도포 순서, 수면 등)을 제안.

5. **전체 루틴 피드백**  
- 루틴 예/아니오 + 선택형 응답을 바탕으로 현재 루틴이 피부 상태와 잘 맞는지 평가.  
- 피부 타입에 따라 잘 맞는 루틴이면 "유지 추천", 부족하거나 잘못된 루틴이면 구체적인 수정 제안.  
- 특정 루틴(예: 클렌징, 각질 제거, 자외선 차단 등)의 빈도와 방법이 적절한지 분석.

6. **현재 루틴에서 주의할 점**  
- 피부 타입 또는 민감도에 비해 **피부를 자극하거나 위험 요소가 있는 루틴**이 있다면 경고하고 수정 방향 제시.  
- 예: 민감성인데 자주 각질 제거 → 피부 장벽 약화 / 지성인데 흡수시간 없이 레이어링 → 모공 막힘  
- 각 주의점마다 피부 타입에 따른 대안 루틴도 제시.

7. **제품 사용 분석 및 추천**  
- 제품 정보가 있을 경우: 성분(예: 시카, 알코올, 실리콘, 정제수 등) 기반으로 피부 타입과 궁합 분석.  
- 제품이 없거나 정보가 적을 경우: "**정보가 부족하여 분석이 어렵다**"는 대신, **피부 상태 기준 추천 성분과 제품군**을 안내.  
- 예: 건성 + 장벽 약함 → 저자극 고보습 (히알루론산, 판테놀, 세라마이드) / 지성 + 트러블 → 피지 억제 및 진정 (살리실산, 나이아신아마이드, 병풀 등)

출력 형식:
- 각 항목은 "**번호. 제목**"으로 시작  
- 각 항목은 **250~350자 이내**, 너무 짧거나 길지 않게  
- **전문적이지만 친절한 톤**, 문장 끝은 단정한 마무리  
- 불필요한 반복 표현 지양

위 정보는 고객에게 바로 전달됩니다.
`;

  try {
    const completion = await openai.chat.completions.create({
      model: "gpt-3.5-turbo",
      messages: [{ role: "user", content: prompt }],
      max_tokens: 1800,
    });

    res.status(200).json({ result: completion.choices[0].message.content });
  } catch (error) {
    console.error("OpenAI 호출 실패:", error);
    res.status(500).json({ error: "GPT 호출 실패" });
  }
}
